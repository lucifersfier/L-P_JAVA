public class Yourinitials {
    public static void main(String[] args) {
        System.out.println(" SSS     A");
        System.out.println("S   S   A A");
        System.out.println("S      A   A");
        System.out.println(" SSS   AAAAA");
        System.out.println("    S  A   A");
        System.out.println("S   S  A   A");
        System.out.println(" SSS   A   A");
    }
}
/*
public: It means that the main method can be accessed from anywhere.
        It is necessary for the JVM to locate and execute the method.

static: The main method is associated with the class, not with any instance of the class.
        This keyword allows the method to be called without creating an instance of the
        class.

void: It indicates that the main method doesn't return any value.

main: This is the name of the method. It is a special name recognized by the JVM
      as the entry point for the program.

String[] args: This parameter allows you to pass command-line arguments to your
               Java program. The args parameter is an array of strings that contains
               any command-line arguments passed to the program. 
               You can use these arguments to customize the behavior of your program.
 
*/